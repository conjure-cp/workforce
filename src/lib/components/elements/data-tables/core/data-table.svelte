<!--suppress ES6UnusedImports -->
<script lang="ts">
  // We need to allow explicit any here because of the way the svelte-headless-table library is written
  /* eslint-disable  @typescript-eslint/no-explicit-any */

  import type { ColumnInitializer } from "$lib/components/elements/data-tables/core/index.ts";

  import { Button } from "$lib/components/ui/button";
  import * as Table from "$lib/components/ui/table";
  import { cn } from "$lib/utils/utils.ts";
  import { ArrowDown, ArrowUp, ArrowUpDown } from "lucide-svelte";
  import { createTable, type ReadOrWritable, Render, Subscribe } from "svelte-headless-table";
  import {
    addHiddenColumns,
    addPagination,
    addSelectedRows,
    addSortBy,
    addTableFilter,
    type PaginationConfig,
    type SortByConfig
  } from "svelte-headless-table/plugins";

  let data: ReadOrWritable<any[]>;
  let columnInitializers: ColumnInitializer[] = [];
  let paginationConfig: PaginationConfig | undefined = undefined;
  let sortByConfig: SortByConfig | undefined = undefined;
  let className: string = "";
  let table = createTable(data, {
    hide: addHiddenColumns(),
    page: addPagination(paginationConfig),
    select: addSelectedRows(),
    sort: addSortBy(sortByConfig),
    tableFilter: addTableFilter({
      fn: ({ filterValue, value }) => {
        return value.toLowerCase().includes(filterValue.toLowerCase());
      }
    })
  });

  const columns = table.createColumns(columnInitializers.map((col) => table.column(col)));
  const { flatColumns, headerRows, pageRows, pluginStates, tableAttrs, tableBodyAttrs } =
    table.createViewModel(columns);
  const { hasNextPage, hasPreviousPage, pageIndex } = pluginStates.page;
  const { filterValue } = pluginStates.tableFilter;
  const { sortKeys } = pluginStates.sort;
  const { hiddenColumnIds } = pluginStates.hide;
  const { selectedDataIds } = pluginStates.select;
  let hideForId: { [key: string]: boolean } = {};

  $: $hiddenColumnIds = Object.entries(hideForId)
    .filter(([, hide]) => hide)
    .map(([id]) => id);

  export {
    className as class,
    columnInitializers,
    data,
    filterValue,
    flatColumns,
    hideForId,
    selectedDataIds,
    sortKeys
  };
</script>

<div class={className}>
  <div class="rounded-md border">
    <Table.Root {...$tableAttrs}>
      <Table.Header>
        {#each $headerRows as headerRow (headerRow.id)}
          <Subscribe let:rowAttrs rowAttrs={headerRow.attrs()}>
            <Table.Row {...rowAttrs}>
              {#each headerRow.cells as cell (cell.id)}
                <Subscribe attrs={cell.attrs()} let:attrs let:props props={cell.props()}>
                  <Table.Head {...attrs} class={cn("text-sm", "[&:has([role=checkbox])]:pl-3")}>
                    {#if props.sort.disabled}
                      <Render of={cell.render()} />
                    {:else}
                      <Button on:click={props.sort.toggle} size="sm" variant="ghost">
                        <Render of={cell.render()} />
                        {#if props.sort.order === "asc"}
                          <ArrowUp
                            class={cn(
                              $sortKeys[0]?.id === cell.id && "text-foreground",
                              "ml-2 h-4 w-4"
                            )}
                          />
                        {:else if props.sort.order === "desc"}
                          <ArrowDown
                            class={cn(
                              $sortKeys[0]?.id === cell.id && "text-foreground",
                              "ml-2 h-4 w-4"
                            )}
                          />
                        {:else}
                          <ArrowUpDown
                            class={cn(
                              $sortKeys[0]?.id === cell.id && "text-foreground",
                              "ml-2 h-4 w-4"
                            )}
                          />
                        {/if}
                      </Button>
                    {/if}
                  </Table.Head>
                </Subscribe>
              {/each}
            </Table.Row>
          </Subscribe>
        {/each}
      </Table.Header>
      <Table.Body {...$tableBodyAttrs}>
        {#each $pageRows as row (row.id)}
          <Subscribe let:rowAttrs rowAttrs={row.attrs()}>
            <Table.Row {...rowAttrs}>
              {#each row.cells as cell (cell.id)}
                <Subscribe attrs={cell.attrs()} let:attrs props={cell.props()}>
                  <Table.Cell class="[&:has([role=checkbox])]:pl-3" {...attrs}>
                    <Render of={cell.render()} />
                  </Table.Cell>
                </Subscribe>
              {/each}
            </Table.Row>
          </Subscribe>
        {/each}
      </Table.Body>
    </Table.Root>
  </div>
  <div class="flex items-center justify-end space-x-4 py-4">
    <Button
      disabled={!$hasPreviousPage}
      on:click={() => ($pageIndex = $pageIndex - 1)}
      size="sm"
      variant="outline"
      >Previous
    </Button>
    <Button
      disabled={!$hasNextPage}
      on:click={() => ($pageIndex = $pageIndex + 1)}
      size="sm"
      variant="outline"
      >Next
    </Button>
  </div>
</div>
